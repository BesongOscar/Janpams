/**
 * Ensures RCTThirdPartyFabricComponentsProvider.h and .mm exist in react-native's
 * React/Fabric folder. React Native 0.76+ always compiles Fabric code, but the
 * codegen that generates these files may not run when New Architecture is disabled,
 * causing: 'React/RCTThirdPartyFabricComponentsProvider.h' file not found.
 *
 * This script writes minimal stub files so the iOS build succeeds. If codegen runs
 * later (e.g. when enabling New Arch), it will overwrite them.
 */

const fs = require('fs');
const path = require('path');

function findReactNativeRoot() {
  try {
    const pkgPath = require.resolve('react-native/package.json', {
      paths: [process.cwd(), __dirname, path.join(__dirname, '..')],
    });
    return path.dirname(pkgPath);
  } catch {
    return null;
  }
}

const STUB_H = `/**
 * Stub for RCTThirdPartyFabricComponentsProvider when codegen has not run.
 * Generated by scripts/ensure-rct-third-party-fabric-provider.cjs
 */

#pragma once

#import <React/RCTComponentViewProtocol.h>

#ifdef __cplusplus
extern "C" {
#endif

Class<RCTComponentViewProtocol> RCTThirdPartyFabricComponentsProvider(const char *name);

#ifdef __cplusplus
}
#endif
`;

const STUB_MM = `/**
 * Stub for RCTThirdPartyFabricComponentsProvider when codegen has not run.
 * Generated by scripts/ensure-rct-third-party-fabric-provider.cjs
 */

#import "RCTThirdPartyFabricComponentsProvider.h"

Class<RCTComponentViewProtocol> RCTThirdPartyFabricComponentsProvider(const char *name) {
  return nil;
}
`;

function main() {
  const rnRoot = findReactNativeRoot();
  if (!rnRoot) {
    console.warn('[ensure-rct-third-party-fabric-provider] react-native not found, skipping');
    return;
  }

  const fabricDir = path.join(rnRoot, 'React', 'Fabric');
  const hPath = path.join(fabricDir, 'RCTThirdPartyFabricComponentsProvider.h');
  const mmPath = path.join(fabricDir, 'RCTThirdPartyFabricComponentsProvider.mm');

  if (!fs.existsSync(fabricDir)) {
    fs.mkdirSync(fabricDir, { recursive: true });
  }

  const writeIfMissing = (filePath, content) => {
    if (!fs.existsSync(filePath)) {
      fs.writeFileSync(filePath, content, 'utf8');
      console.log('[ensure-rct-third-party-fabric-provider] Created', path.relative(rnRoot, filePath));
    }
  };

  writeIfMissing(hPath, STUB_H);
  writeIfMissing(mmPath, STUB_MM);
}

main();
